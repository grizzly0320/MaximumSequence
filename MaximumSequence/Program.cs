string[] massive = str.Split(' ').ToArray();
//Создаем числовой массив
int[] mas = new int[str.Length];
// Перебираем массив
for (int i = 0; i < massive.Length; i++)
{
    // Преобразуем элементы из massive из string в int 
    mas[i] = int.Parse(massive[i]);
}
//Созздадим массив на два элемента записи концов наибольшой последовательности
int[] result = new int[2];
//Переменные для записи концов последовательности
int min = 0, max = 0;
//Переменные для записи длины последовательности и наибольшей последоваетельности
int range = 0, max_range = 0;
//Перебираем массив чисел
for (int i = 0; i < mas.Length; i++)
{
    // Если проверяемый элемент не первый в массиве
    if (i != 0)
    {
        // Если проверяемый эелемент больше предыдущего
        if (mas[i] > mas[i - 1])
        {
            //Перезаписываем правый конец последовательности
            max = mas[i];
        }
        //Если рассматриваемый элемент больше предыдущего
        else if (mas[i] < mas[i - 1])
        {

            //Узнаем длину рассматриваемой последовательности
            range = max - min;
