                result[0] = min;
                //Во второй элемент массива записываем правый конец наибольшой последовательности
                result[1] = max;
            }
            min = mas[i];
        }
    }
    //Если рассматриваемый элемент первый
    else
    {
        //В правый эелемент последовательности записываем первый эелемент массива
